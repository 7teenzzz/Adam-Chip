/*********************** КАМЕРА OV5640 **********************/

#define PWDN_GPIO_NUM  -1
#define RESET_GPIO_NUM -1
#define XCLK_GPIO_NUM  15
#define SIOD_GPIO_NUM  4
#define SIOC_GPIO_NUM  5

#define Y9_GPIO_NUM    16
#define Y8_GPIO_NUM    17
#define Y7_GPIO_NUM    18
#define Y6_GPIO_NUM    12
#define Y5_GPIO_NUM    10
#define Y4_GPIO_NUM    8
#define Y3_GPIO_NUM    9
#define Y2_GPIO_NUM    11
#define VSYNC_GPIO_NUM 6
#define HREF_GPIO_NUM  7
#define PCLK_GPIO_NUM  13


/*********************** I2S МИКРОФОНЫ (INMP441 x2) ***********************
 * Оба микрофона на одной шине
 * L/R пинами разделяются каналы
 *************************************************************************/

#define I2S_MIC_BCLK   41   // SCK / BCLK
#define I2S_MIC_WS     42   // LRCLK / WS
#define I2S_MIC_SD     47   // DATA


/*********************** I2S DAC (PCM5102) *******************************
 * MCLK не используется
 *************************************************************************/

#define I2S_DAC_BCLK   38   // BCLK
#define I2S_DAC_LRCK   39   // LR / WS
#define I2S_DAC_DATA   40   // SD (DIN)


/*********************** ДАТЧИКИ ***************************************/

#define LIGHT_SENSOR_PIN   1    // TEMT6000 (ADC)
#define MOTION_SENSOR_PIN  21   // BTE16-19 (digital)


/*********************** I2C (PCA9685 PWM) ******************************
 * Управление моторами, светом и прочей нагрузкой
 *************************************************************************/

#define I2C_SDA_PIN   43
#define I2C_SCL_PIN   44


/*********************** РЕЗЕРВ / СВОБОДНЫЕ ПИНЫ ***********************
 * Можно использовать при расширении
 *************************************************************************/

#define FREE_GPIO_1   2
#define FREE_GPIO_2   3
#define FREE_GPIO_3   14


/*********************** ПРИМЕЧАНИЯ *************************************
 *
 * 1. Пины камеры (4–13, 15–18) не использовать нигде ещё
 *
 * 2. GPIO35–37 заняты PSRAM — не трогать
 *
 * 3. GPIO0 / 45 / 46 — загрузочные, избегать
 *
 * 4. GPIO19 / 20 — USB, не трогать если используется USB
 *
 * 5. Вся силовая нагрузка (моторы, LED) управляется через PCA9685 + MOSFET
 *
 * 6. PCM5102:
 *    - MCLK не подключается
 *    - питание 3.3V
 *
 * 7. INMP441:
 *    - оба микрофона на одной линии
 *    - L/R определяет канал
 *
 ***********************************************************************/
